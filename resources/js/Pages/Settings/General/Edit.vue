<script lang="ts" setup>
import { Select, SelectContent, SelectItem, SelectSeparator, SelectTrigger, SelectValue } from '@/Components/ui/select'
import { Switch } from '@/Components/ui/switch'
import { Head, router, useForm } from '@inertiajs/vue3'
import { useDebounceFn } from '@vueuse/core'
import { AppWindowMac, CalendarMinus, Eye, Globe, KeyRound, Languages, SunMoon } from 'lucide-vue-next'
import { ref, watch } from 'vue'

const props = defineProps<{
    openAtLogin?: boolean
    theme?: string
    showTimerOnUnlock?: boolean
    holidayRegion?: string
    locale: string
    appActivityTracking?: boolean
    timezones?: string[]
    timezone: string
}>()

const form = useForm({
    openAtLogin: props.openAtLogin ?? false,
    theme: props.theme ?? 'system',
    showTimerOnUnlock: props.showTimerOnUnlock ?? false,
    holidayRegion: props.holidayRegion ?? '',
    locale: props.locale,
    appActivityTracking: props.appActivityTracking ?? false,
    timezone: props.timezone
})

const submit = () => {
    router.flushAll()
    form.patch(route('settings.general.update'), {
        preserveScroll: true,
        preserveState: true
    })
}
const holidayCheck = ref(props.holidayRegion !== null)

const debouncedSubmit = useDebounceFn(submit, 500)
watch(
    () => [
        form.theme,
        form.locale,
        form.openAtLogin,
        form.showTimerOnUnlock,
        form.holidayRegion,
        form.appActivityTracking,
        form.timezone
    ],
    debouncedSubmit,
    { deep: true }
)
watch(holidayCheck, () => {
    if (holidayCheck.value === false) {
        form.holidayRegion = ''
    }
})
</script>

<template>
    <Head title="Settings - General" />
    <div class="mb-4 flex h-8 items-center justify-between gap-4">
        <div class="text-foreground/80 text-base font-medium">{{ $t('app.general settings') }}</div>
    </div>
    <div>
        <div class="flex items-center space-x-4 py-4">
            <KeyRound />
            <div class="flex-1 space-y-1">
                <p class="text-sm leading-none font-medium">
                    {{ $t('app.start at login') }}
                </p>
            </div>
            <Switch v-model="form.openAtLogin" />
        </div>
        <div class="flex items-start space-x-4 py-4">
            <Languages />
            <div class="flex flex-1 items-center gap-4 space-y-1">
                <p class="flex-1 text-sm leading-none font-medium">
                    {{ $t('app.language') }}
                </p>
                <Select size="5" v-model="form.locale">
                    <SelectTrigger class="w-1/2">
                        <SelectValue :placeholder="$t('app.language')" />
                    </SelectTrigger>
                    <SelectContent>
                        <SelectItem value="de-DE">
                            {{ $t('app.german') }}
                        </SelectItem>
                        <SelectItem value="en-GB">
                            {{ $t('app.english (UK)') }}
                        </SelectItem>
                        <SelectItem value="en-US">
                            {{ $t('app.english (US)') }}
                        </SelectItem>
                    </SelectContent>
                </Select>
            </div>
        </div>
        <div class="flex items-start space-x-4 py-4">
            <Globe />
            <div class="flex flex-1 items-center gap-4 space-y-1">
                <p class="flex-1 text-sm leading-none font-medium">
                    {{ $t('app.timezone') }}
                </p>
                <Select size="5" v-model="form.timezone">
                    <SelectTrigger class="w-1/2">
                        <SelectValue :placeholder="$t('app.timezone')" />
                    </SelectTrigger>
                    <SelectContent>
                        <SelectItem :key="timezone" :value="timezone" v-for="timezone in props.timezones">
                            {{ timezone }}
                        </SelectItem>
                    </SelectContent>
                </Select>
            </div>
        </div>
        <div class="flex items-start space-x-4 py-4">
            <SunMoon />
            <div class="flex flex-1 gap-4">
                <div class="flex-1 space-y-1">
                    <p class="text-sm leading-none font-medium">
                        {{ $t('app.appearance') }}
                    </p>
                    <p class="text-muted-foreground text-sm text-balance">
                        {{ $t('app.choose the appearance of the application.') }}
                    </p>
                </div>

                <Select size="5" v-model="form.theme">
                    <SelectTrigger class="w-1/2">
                        <SelectValue :placeholder="$t('app.appearance')" />
                    </SelectTrigger>
                    <SelectContent>
                        <SelectItem value="system">
                            {{ $t('app.system') }}
                        </SelectItem>
                        <SelectItem value="light">
                            {{ $t('app.light') }}
                        </SelectItem>
                        <SelectItem value="dark">
                            {{ $t('app.dark') }}
                        </SelectItem>
                    </SelectContent>
                </Select>
            </div>
        </div>
        <div class="flex items-start space-x-4 py-4">
            <Eye />
            <div class="flex-1 space-y-1">
                <p class="text-sm leading-none font-medium">
                    {{ $t('app.show timer automatically') }}
                </p>
                <p class="text-muted-foreground text-sm text-balance">
                    {{ $t('app.when the computer is unlocked, the timer can be displayed.') }}
                </p>
            </div>
            <Switch class="self-center" v-model="form.showTimerOnUnlock" />
        </div>

        <div class="flex items-start space-x-4 py-4">
            <AppWindowMac />
            <div class="flex-1 space-y-1">
                <p class="text-sm leading-none font-medium">
                    {{ $t('app.record app activities') }}
                    <span
                        class="text-destructive bg-destructive/20 ml-2 rounded px-1 py-0.5 text-xs"
                        v-if="$page.props.environment === 'Windows'"
                    >
                        {{ $t('app.not available on windows') }}
                    </span>
                </p>
                <p class="text-muted-foreground text-sm">
                    {{ $t('app.records your app activity and saves which app you were active in and for how long.') }}
                </p>
            </div>
            <Switch
                :disabled="$page.props.environment !== 'Darwin'"
                class="self-center"
                v-model="form.appActivityTracking"
            />
        </div>

        <div class="flex items-start space-x-4 py-4">
            <CalendarMinus />
            <div class="flex-1 space-y-1">
                <div class="flex items-center gap-10">
                    <div class="flex-1 space-y-1">
                        <p class="text-sm leading-none font-medium">
                            {{ $t('app.consider public holidays') }}
                        </p>
                        <p class="text-muted-foreground text-sm">
                            {{ $t('app.working hours on public holidays are fully credited.') }}
                        </p>
                    </div>
                    <Switch v-model="holidayCheck" />
                </div>

                <Select size="5" v-if="holidayCheck" v-model="form.holidayRegion">
                    <SelectTrigger class="mt-2 ml-auto w-1/2">
                        <SelectValue placeholder="Region" />
                    </SelectTrigger>
                    <SelectContent>
                        <SelectItem value="DE">Deutschland</SelectItem>
                        <SelectSeparator />
                        <SelectItem value="DE-BB"> Brandenburg</SelectItem>
                        <SelectItem value="DE-BE"> Berlin</SelectItem>
                        <SelectItem value="DE-BW"> Baden-Württemberg</SelectItem>
                        <SelectItem value="DE-BY"> Bayern</SelectItem>
                        <SelectItem value="DE-HB"> Bremen</SelectItem>
                        <SelectItem value="DE-HE"> Hessen</SelectItem>
                        <SelectItem value="DE-HH"> Hamburg</SelectItem>
                        <SelectItem value="DE-MV"> Mecklenburg-Vorpommern</SelectItem>
                        <SelectItem value="DE-NI"> {{ $t('app.never') }}dersachsen</SelectItem>
                        <SelectItem value="DE-NW"> Nordrhein-Westfalen</SelectItem>
                        <SelectItem value="DE-RP"> Rheinland-Pfalz</SelectItem>
                        <SelectItem value="DE-SH"> Schleswig-Holstein</SelectItem>
                        <SelectItem value="DE-SL"> Saarland</SelectItem>
                        <SelectItem value="DE-SN"> Sachsen</SelectItem>
                        <SelectItem value="DE-ST"> Sachsen-Anhalt</SelectItem>
                        <SelectItem value="DE-TH"> Thüringen</SelectItem>
                    </SelectContent>
                </Select>
            </div>
        </div>
    </div>
</template>
